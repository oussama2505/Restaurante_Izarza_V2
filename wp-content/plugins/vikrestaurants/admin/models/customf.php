/**
 * VikRestaurants custom field model.
 *
 * @since 1.9
 */
class VikRestaurantsModelCustomf extends JModelVRE
{
	/**
	 * Basic item loading implementation.
	 *
	 * @param   mixed    $pk   An optional primary key value to load the row by, or an array of fields to match.
	 *                         If not set the instance property value is used.
	 * @param   boolean  $new  True to return an empty object if missing.
	 *
	 * @return  mixed    The record object on success, null otherwise.
	 */
	public function getItem($pk, $new = false)
	{
		$field = parent::getItem($pk, $new);

		if (!$field)
		{
			return null;
		}

		if (!$field->id)
		{
			// text type by default
			$field->type = 'text';
			// set default values for the new custom field
			$field->group = VikRestaurantsHelper::getDefaultGroup();
		}

		return $field;
	}

	/**
	 * Extend delete implementation to delete any related records
	 * stored within a separated table.
	 *
	 * @param   mixed    $ids  Either the record ID or a list of records.
	 *
	 * @return  boolean  True on success, false otherwise.
	 */
	public function delete($ids)
	{
		// only int values are accepted
		$ids = array_map('intval', (array) $ids);

		// invoke parent first
		if (!parent::delete($ids))
		{
			// nothing to delete
			return false;
		}

		$db = JFactory::getDbo();

		// load any assigned translation
		$q = $db->getQuery(true)
			->select($db->qn('id'))
			->from($db->qn('#__vikrestaurants_lang_customf'))
			->where($db->qn('id_customf') . ' IN (' . implode(',', $ids) . ')' );

		$db->setQuery($q);

		if ($languages = $db->loadColumn())
		{
			// delete all the translations that belong to the removed custom fields
			JModelVRE::getInstance('langcustomf')->delete($languages);
		}

		return true;
	}
}
